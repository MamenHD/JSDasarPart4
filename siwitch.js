// Menggunakan Switch

const fruit = 'MamenHD';

switch (fruit) {
  case 'banana': // isi dari nilai
    console.log('I am a banana.'); // perintah yang akan muncul
    break; // memberhentikan perintah
  case 'apple':
    console.log('I am an apple.');
    break;
  case 'orange':
    console.log('I am an orange.');
    break;
  case 'strawberry':
    console.log('I am a strawberry.');
    break;
  default:
    console.log('I am not a fruit. I am a programmer.');
}
